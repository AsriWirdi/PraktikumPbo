
public abstract class Manusia {
    // fields
    protected String nama, alamat;
    
    // getter
    protected String getNama(){
        return nama;
    }
    
    protected String getAlamat(){
        return alamat;
    }

    // setter
    protected void setNama(String nama){
        this.nama = nama;
    }

    protected void setAlamat(String alamat){
        this.alamat = alamat;
    }


}
